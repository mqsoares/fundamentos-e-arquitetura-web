export const Dark = () => {
    const teste = document.querySelector("html");
    teste?.getAttribute("data-bs-theme");
    teste?.setAttribute("data-bs-theme", "dark");
};
export const Light = () => {
    const teste = document.querySelector("html");
    teste?.getAttribute("data-bs-theme");
    teste?.setAttribute("data-bs-theme", "light");
};

